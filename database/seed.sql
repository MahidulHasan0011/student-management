-- marks deterministic formula:
--   base   = 90 - (student number - 1) * 3   → student 001 সবচেয়ে ভালো, 010 সবচেয়ে কম (পরিষ্কার rank)
--   + subj.adj                                → subject ভেদে সামান্য তারতম্য
--   + (exam_date থেকে দিনের সংখ্যা)/120        → পরের exam-এ সামান্য উন্নতি
--   GREATEST/LEAST দিয়ে 33–99 এর মধ্যে clamp করা
-- শুধু PUBLISHED + UNIT_TEST/MIDTERM/FINAL exam নেওয়া হয়; Admission Test (উপরে) এতে আসে না।
INSERT INTO public.exam_results (exam_id, student_id, subject_id, marks, grade)
SELECT
  e.id,
  s.id,
  subj.subject_id,
  m.val,
  CASE
    WHEN m.val >= 80 THEN 'A+'
    WHEN m.val >= 70 THEN 'A'
    WHEN m.val >= 60 THEN 'A-'
    WHEN m.val >= 50 THEN 'B'
    WHEN m.val >= 40 THEN 'C'
    ELSE 'F'
  END
FROM public.exams e
JOIN public.student_enrollments se
  ON se.class_id = e.class_id
 AND se.academic_session_id = e.academic_session_id
